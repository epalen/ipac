  <div class="panel-heading"><h4>Iniciativa</h4></div>
  <div class="panel-body">
    <p class="text-justify">
      <font size="4">
        La Iniciativa Participativa Anticorrupción –IPAC- es un mecanismo permanente de interacción entre el gobierno, sociedad civil, sector empresarial, asociaciones sin fines de lucro, academias, gremios profesionales y ciudadanía en general, para la coordinación y desarrollo de iniciativas que mejoren los niveles de transparencia, prevengan la corrupción en la administración pública, fomenten la rendición de cuentas y permitan al Estado dar respuesta oportuna a las denuncias que se generen en el marco de este mecanismo.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        La necesidad de la creación de un portal IPAC, nace como una forma de que los ciudadanos estén más en contacto con los avances que presenta el Gobierno Dominicano en materia de transparencia, gobierno abierto, y libre acceso a las informaciones de las instituciones públicas.  Mediante este portal se aspira a que los ciudadanos tengan acceso a una plataforma sobre la cual puedan formular sus demandas, y que estas sean respondidas por los órganos competentes responsables de cumplir con cada necesidad.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        La iniciativa integra un comité organizador conformado por actores claves del gobierno, la empresa privada y la sociedad civil dominicana, asegurando que la misma sea una iniciativa de carácter nacional.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        <strong>Participantes</strong>
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        Este portal estará conformado por la ciudadanía en general, quienes formularan sus propuestas, ya sean sugerencias, demandas, quejas y/o cualquier tipo de informaciones públicas, del mismo modo, las instituciones serán las encargadas de dar respuestas a estas inquietudes, todo lo cual estará bajo la supervisión de la Dirección General de Ética e Integridad Gubernamental (DIGEIG), órgano rector encargado de dar seguimiento a estas propuestas, para que los mismos mejoren la transparencia y el acceso a la administración pública.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        <strong>Objetivo de la creación del portal</strong>
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        Crear un portal de interacción entre el gobierno, sociedad civil, sector empresarial, asociaciones sin fines de lucro, academias, gremios profesionales y ciudadanía en general, para la coordinación y desarrollo de iniciativas que mejoren los niveles de transparencia, prevengan la corrupción en la administración pública, fomenten la rendición de cuentas y permitan al Estado dar respuestas a las denuncias que se generen en el marco de este mecanismo.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        Con la finalidad de contribuir a mejorar los niveles de transparencia de la sociedad dominicana, se pone en marcha un proceso de concertación de los distintos actores representativos de la sociedad, organizados por temas de interés. Los objetivos de esta iniciativa son los siguientes:
      </font>
    </p>
    <p class="text-justify">
        <font size="4">
          <ul>
            <br>
            <li>
                Motivar la participación de todos los sectores de la vida nacional a fin de crear las condiciones propicias para la presentación de sus propuestas e inquietudes, en el marco de la transparencia y la prevención de la corrupción.
            </li>
            <br>
            <li>
                Promover la formulación de iniciativas en función de las necesidades identificadas por cada sector, propiciando así la interacción y concertación de los diversos sectores para la implementación de las medidas resultantes de esta iniciativa.
            </li>
            <br>
            <li>
                Crear una cultura permanente de auto-revisión de la gestión pública para mantener los estándares de transparencia y el buen manejo de los bienes públicos.
            </li>
            <br>
            <li>
                Velar por el cumplimiento de las responsabilidades que asuman los distintos entes y órganos administrativos que forman parte de la iniciativa.
            </li>
            <br>
            <li>
                Contribuir al mejoramiento de los niveles de transparencia a través del control social horizontal.
            </li>
          </ul>
        </font>
    </p>
  </div>
</div>