  <div class="panel-heading"><h4>Transparencia</h4></div>
  <div class="panel-body">
    <p class="text-justify">
      <font size="4">
        <strong>Ley general de libre acceso a la información pública</strong>
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        La Ley General 200-04 de Libre Acceso a la Información Pública (LGLAIP) fue aprobada por el Congreso Nacional el día 13 de julio de 2004 y promulgada por el Poder Ejecutivo el día 28 del mismo mes y año.  El año siguiente, se estableció su reglamentación por medio del Decreto 130-05.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        El artículo no. 1 de la LGLAIP establece que “toda persona tiene derecho a solicitar y a recibir información completa, veraz, adecuada y oportuna, de cualquier órgano del Estado Dominicano, y de todas las sociedades anónimas, compañías anónimas o compañías por acciones con participación estatal”.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        <strong>Información pública</strong>
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        Todo tipo de información creada u obtenida por la Administración Pública o por cualquier otro órgano que ejerza funciones públicas o ejecute presupuestos públicos, siempre y cuando no tenga restricción de acceso de acuerdo a la legislación vigente.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        <strong>Mi derecho a saber</strong>
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        El artículo no.3 de la LGLAIP establece que todos los actos y actividades de la Administración Pública, centralizada y descentralizada, incluyendo los actos y actividades administrativas de los Poderes Legislativo y Judicial, así como la información referida a su funcionamiento estarán sometidos a publicidad, en consecuencia, será obligatorio para el Estado dominicano y todos sus poderes y organismos autónomos, autárquicos, centralizados y/o descentralizados, la presentación de un servicio permanente y actualizado de información referida a:
      </font>
    </p>
    <p class="text-justify">
        <font size="4">
          <ul>
            <br>
            <li>
              Presupuestos y cálculos de recursos y gastos aprobados, su evolución y estado de ejecución;
            </li>
            <br>
            <li>
              Programas y proyectos, sus presupuestos, plazos, ejecución y supervisión;
            </li>
            <br>
            <li>
              Llamado a licitaciones, concursos, compras, gastos y resultados;
            </li>
            <br>
            <li>
              Listados de funcionarios, legisladores, magistrados, empleados, categorías, funciones y remuneraciones, y la declaración jurada patrimonial cuando su presentación corresponda por ley;
            </li>
            <br>
            <li>
              Listado de beneficiarios de programas asistenciales, subsidios, becas, jubilaciones, pensiones y retiros; 
            </li>
            <br>
            <li>
              Estado de cuentas de la deuda pública, sus vencimientos y pagos; 
            </li>
            <br>
            <li>
              Leyes, decretos, resoluciones, disposiciones, marcos regulatorios y cualquier otro tipo de normativa; 
            </li>
            <br>
            <li>
              Índices, estadísticas y valores oficiales; 
            </li>
            <br>
            <li>
              Marcos regulatorios legales y contractuales para la prestación de los servicios públicos, condiciones, negociaciones, cuadros tarifarios, y sanciones; 
            </li>
            <br>
            <li>
              Toda otra información cuya disponibilidad al público sea dispuesta en leyes especiales.
            </li>
          </ul>
        </font>
    </p>
    <p class="text-justify">
      <font size="4">
        <strong>Procedimientos</strong>
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        El acceso a la información pública es en principio gratuito. El artículo no.7 de la ley 200-04 establece que toda solicitud debe hacerse por escrito y deberá contener para su tramitación los siguientes requisitos:
      </font>
    </p>
    <p class="text-justify">
        <font size="4">
          <ul>
            <br>
            <li>
              Nombre completo y calidades de la persona que realiza la gestión;
            </li>
            <br>
            <li>
              Identificación clara y precisa de los datos e informaciones que requiere;
            </li>
            <br>
            <li>
              Identificación de la autoridad pública que posee la información;
            </li>
            <br>
            <li>
              Motivación de las razones por las cuales se requieren los datos e informaciones solicitadas;
            </li>
            <br>
            <li>
              Lugar o medio para recibir las notificaciones. 
            </li>
          </ul>
        </font>
    </p>
    <p class="text-justify">
      <font size="4">
        Toda solicitud debe de ser satisfecha en un plazo no mayor quince (15) días hábiles, que podrá prorrogarse en forma excepcional por otros diez (10) días hábiles.
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        <strong>¿Dónde ejerzo mi derecho de acceso a la información pública?</strong>
      </font>
    </p>
    <p class="text-justify">
      <font size="4">
        El ejercicio del derecho de acceso a la información pública se hará efectivo ante las Oficina de acceso a la información (OAI), las cuales cuentan con una Responsable de Acceso a la Información Pública (RAI) cuya función principal es realizar las gestiones necesarias para garantizar el más amplio acceso de la persona a la información pública.
      </font>
